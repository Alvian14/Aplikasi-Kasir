    public boolean logins(String usernameOrID, String password){
        USERNAME = usernameOrID;
        try{
            
            String query = 
        String.format("SELECT tbl_karyawan.kd_karyawan, tbl_karyawan.nama_karyawan, tbl_login.username, "
        + "tbl_login.password FROM tbl_karyawan JOIN tbl_login ON tbl_karyawan.kd_karyawan = tbl_login.kd_karyawan "
        + "WHERE tbl_login.username = '%s' OR tbl_karyawan.kd_karyawan = '%s'", USERNAME, usernameOrID);
            
            Connection conn = (Connection) koneksi.configDB();
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(query);
            
            if(res.next()){
                
                if(password.equals(res.getString("tbl_login.password"))){
                    JFramelogin.NAMA_USER = res.getString("tbl_karyawan.nama_karyawan");
                   
                    conn.close();
                    return true;
                }else{
//                    Message.showWarning(this, "Password Anda tidak cocok!");
                    JOptionPane.showMessageDialog(null, "Password Anda tidak cocok!");
                }
            }else{
//                Message.showWarning(this, String.format("'%s' Username tersebut tidak ditemukan!", usernameOrID));
                JOptionPane.showMessageDialog(null, "'%s' Username tersebut tidak ditemukan");
            }
        }catch(SQLException ex){
//            Message.showException(this, ex);
            JOptionPane.showMessageDialog(null, ex);
        }
        return false;
    }
