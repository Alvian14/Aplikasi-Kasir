    private String getKd(String username){
        try{
            String sql = "SELECT kd_karyawan FROm tbl_karyawan WHERE username = '" + username + "'";
            Connection c = (Connection) koneksi.configDB();
            Statement s = c.createStatement();
            ResultSet r = s.executeQuery(sql);
            
            if(r.next()){
                return r.getString("kd_karyawan");
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        return null;
    }
    
    private void getDataKaryawan(String username){
        try{
            String kd = this.getKd(username);
            String sql = "SELECT nama_karyawan FROM tbl_karyawan WHERE username = '"+username+"'";
            Connection c = (Connection) koneksi.configDB();
            Statement s = c.createStatement();
            ResultSet r = s.executeQuery(sql);
            
            if(r.next()){
                karyawan = kd + " " + r.getString("nama_karyawan");
                System.out.println();
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }
    
    
