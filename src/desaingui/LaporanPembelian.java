    private void laporanharianMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_laporanharianMouseClicked
        // TODO add your handling code here:
        String selectedRow = this.laporanharian.getValueAt(laporanharian.getSelectedRow(),0).toString();
        
        String[] columnNames = {"Nama Barang", "Harga Barang", "Jumlah Barang"};
        DefaultTableModel tablemodel = new DefaultTableModel(columnNames, 0);
        
        try {
            Connection c = (Connection) koneksi.configDB();
            Statement s = c.createStatement();
            ResultSet rs = s.executeQuery("SELECT * FROM detail_belibarang WHERE kd_pembelian = '"+ selectedRow+"'");
            
            while (rs.next()) {
                String namabarang = rs.getString("nama_barangbeli"),
                       hargabarang = rs.getString("harga_barangbeli"),
                       jumlahbarang = rs.getString("jumlah_barangbeli");
                
                //create single array of one row wort of java
                String[] data = {namabarang,hargabarang,jumlahbarang};
                 tablemodel.addRow(data);       
            }
        }  catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error : " + ex.getMessage());
        }
        this.tabeldetail.setModel(tablemodel);
        
        String selectedsupp = this.laporanharian.getValueAt(laporanharian.getSelectedRow(),1).toString();
        
        try{
            String sql = "SELECT nama_supplier FROM tbl_supplier WHERE kd_supplier ='" + selectedsupp+"'";
             Connection c = (Connection) koneksi.configDB();
            Statement s = c.createStatement();
            ResultSet r = s.executeQuery(sql);
            
            if(r.next()) {
                String namasupp = r.getString("nama_supplier");
                this.labelsupplier.setText(namasupp);
                
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error : " + ex.getMessage());
        }
    }//GEN-LAST:event_laporanharianMouseClicked

