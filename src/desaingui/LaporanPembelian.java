    private void butontanggalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butontanggalActionPerformed
        // TODO add your handling code here:
        String[] columnNames = {"Kd Pembelian", "Kd Supplier", "Total Harga Barang", "Waktu Pembelian",};
        DefaultTableModel tablemodel = new DefaultTableModel(columnNames, 0);
        
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String dateTrans = sdf.format(jDateChooser1.getDate());
        
        try {
            Connection c = (Connection) koneksi.configDB();
            Statement s = c.createStatement();
            ResultSet rs = s.executeQuery("SELECT *FROM tbl_pembelian WHERE waktu_beli = '" + dateTrans + "'ORDER BY kd_pembelian DESC ");
            
            while (rs.next()) {
                String Kd_Pembelian = rs.getString("kd_pembelian"),
                        Kd_Supplier = rs.getString("kd_Supplier"),
                        Total_Harga = rs.getString("total_hargabarangbelii"),
                        Waktu_Pembelian = rs.getString("waktu_beli");
                
            //create a singlle array of one roes worth of data
                String[] data = {Kd_Pembelian, Kd_Supplier, Total_Harga, Waktu_Pembelian};
                tablemodel.addRow(data);
                this.laporanharian.setModel(tablemodel);
            }
            
                        
                
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error : " + ex.getMessage());
        }
    }//GEN-LAST:event_butontanggalActionPerformed

